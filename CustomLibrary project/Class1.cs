namespace CustomLibrary_project;

public class Average
{
    public static double MyAverage(double num1, double num2)
    {
       
        return (num1 + num2) / 2;
        
    }

    public static double MyAverage(double num1, double num2, double num3)
    {
       
        return (num1 + num2 + num3) / 3;
       
    }
}

